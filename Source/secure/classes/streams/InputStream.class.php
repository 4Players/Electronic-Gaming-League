class InputStream
{
	# -[ variables ]-
	var $oSocket	= null;

	
	# -[ functions ]-
	
	
	# =========================================================================
	# PUBLIC
	# =========================================================================
	
	//-------------------------------------------------------------------------------
	// Purpose: constructor
	// Output : -
	//-------------------------------------------------------------------------------
	function InputStream ( &$oSocket )
	{
		/*
			
		*/
		if( get_parent_class($oSocket) == 'socket' )
		{
			$this->oSocket = &$oSocket;
		}//if
	}
	
	
	//-------------------------------------------------------------------------------
	// Purpose:
	// Output :
	//-------------------------------------------------------------------------------
	function readInt()
	{
		// read 4 bytes(chars) buffer from input stream
		// convert single bytes to 32 bit integer value
		return rb_integer( $this->oSocket->ReceiveBuffer( 4 /*4 bytes*/ ));
	}
	
	
	
	//-------------------------------------------------------------------------------
	// Purpose:
	// Output :
	//-------------------------------------------------------------------------------
	function readFloat()
	{
	}
	
	
	
	//-------------------------------------------------------------------------------
	// Purpose:
	// Output :
	//-------------------------------------------------------------------------------
	function readString($len)
	{
		return $this->oSocket->ReceiveBuffer($len);
	}

};



